def main():
    # Запрашиваем количество учебных дней
    while True:
        try:
            days = int(input("Введите количество учебных дней (не более 7): "))
            if 1 <= days <= 7:
                break
            else:
                print("Ошибка: количество дней должно быть от 1 до 7.")
        except ValueError:
            print("Ошибка: введите целое число.")

    total_hours = 0  # Переменная для подсчета общего количества часов учёбы

    # Запрашиваем число часов для каждого учебного дня
    for i in range(1, days + 1):
        while True:
            try:
                hours = float(input(f"Введите количество часов учебы для дня {i}: "))
                if hours < 0:
                    print("Ошибка: количество часов не может быть отрицательным. Попробуйте снова.")
                else:
                    total_hours += hours
                    break
            except ValueError:
                print("Ошибка: введите число.")

    # Выводим общее количество часов учебы
    print(f"\nОбщее количество часов учебы за неделю: {total_hours} часов.")

if __name__ == "__main__":
    main()