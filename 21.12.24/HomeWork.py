def input_cost():
    while True:
        try:
            cost = float(input("Введите стоимость материала (положительное число): "))
            if cost < 0:
                print("Ошибка: стоимость не может быть отрицательной. Попробуйте снова.")
            else:
                return cost
        except ValueError:
            print("Ошибка: ввод должен быть числом. Попробуйте снова.")


# Запрос типа учебного материала
material_type = input("Введите тип учебного материала (например, книга или видео): ")

# Запрос стоимости материала с обработкой ошибок
cost = input_cost()

# Запрос категории материала
category = input("Введите категорию материала: ")

# Вывод информации о добавленном материале
print("\nМатериал добавлен:")
print("Тип:", material_type)
print("Стоимость:", cost)
print("Категория:", category)